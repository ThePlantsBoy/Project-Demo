// Inclinación 3D fluida al mover el dedo sobre la imagen expandida
panelImg.addEventListener('touchmove', (e) => {
    if (!panelImg.classList.contains('expanded')) return;
    const toque = e.touches[0];
    const rect = panelImg.getBoundingClientRect();
    
    const x = toque.clientX - rect.left;
    const y = toque.clientY - rect.top;
    
    const centroX = rect.width / 2;
    const centroY = rect.height / 2;
    
    const rotarX = ((centroY - y) / centroY) * 15;
    const rotarY = ((x - centroX) / centroX) * 15;
    
    panelImg.style.transform = `translate(-50%, -50%) scale(1.6) rotateX(${rotarX}deg) rotateY(${rotarY}deg)`;
});


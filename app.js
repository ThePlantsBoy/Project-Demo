function obtenerCookie(nombre) {

    const cookies = document.cookie.split(";");

    for (let cookie of cookies) {

        let [clave, valor] = cookie.trim().split("=");

        if (clave === nombre) {
            return valor;
        }
    }

    return null;
}

function crearCuenta() {

    const usuario = document.getElementById("usuario-registrado");

    const password = document.getElementById("usuario-contraseña");

    let error = false;

    usuario.classList.remove("input-error");
    password.classList.remove("input-error");

    if (usuario.value.trim() === "") {

        usuario.classList.add("input-error");
        error = true;
    }

    if (password.value.trim() === "") {

        password.classList.add("input-error");
        error = true;
    }

    if (error) return;

    let cuentas = [];

    const cookieCuentas = obtenerCookie("cuentas");
    
    if (cookieCuentas) {
        cuentas = JSON.parse(
            decodeURIComponent(cookieCuentas)
        );
    }
    const existe = cuentas.some(cuenta => cuenta.usuario === usuario.value);

    if (existe) {
    document.querySelector('.exist-account-message').classList.add('show');
    setTimeout(() =>{
        document.querySelector('.exist-account-message').classList.remove('show')
    }, 10000);
    return;
}

    cuentas.push({
        usuario: usuario.value,
        password: password.value
    });

    document.cookie = `cuentas=${encodeURIComponent(JSON.stringify(cuentas))}; max-age=50000; path=/`;

    AccountCreatedMessage.classList.add('show')

    setTimeout(() =>{
        AccountCreatedMessage.classList.remove('show')
    }, 10000)
}

function login() {

    const usuarioIngresado =
        document.getElementById("usuario-login").value;

    const passwordIngresada =
        document.getElementById("usuario-contraseña-login").value;

    const cookieCuentas = obtenerCookie("cuentas");

    if (!cookieCuentas) {
        document.querySelector('.no-account-message').classList.add('show');
        setTimeout(() =>{
            document.querySelector('.no-account-message').classList.remove('show')
        }, 10000);
        return;
    }

    const cuentas = JSON.parse(
        decodeURIComponent(cookieCuentas)
    );

    const cuenta = cuentas.find(
        cuenta =>
            cuenta.usuario === usuarioIngresado && cuenta.password === passwordIngresada
    );

    if (cuenta) {
        document.cookie =
            `sesion=${usuarioIngresado}; max-age=50000; path=/`;
        document.querySelector('.login-message').classList.add('show');
        setTimeout(() =>{
            document.querySelector('.login-message').classList.remove('show');
            window.location.reload();
        }, 5000);

    } else {
        document.querySelector('.inccorect-info-message').classList.add('show')
        setTimeout(() =>{
            document.querySelector('.inccorect-info-message').classList.remove('show')
        }, 10000)
    }
}


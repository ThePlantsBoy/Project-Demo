// --- Añadido 2: Lógica y Visibilidad de los Botones del Lector de Capítulos ---
function actualizarBotonesLector() {
    const comic = bdComics[currentComicId];
    if (!comic) return;
    const totalCaps = comic.capitulos.length;

    if (totalCaps <= 1) {
        btnReaderPrev.classList.add('hidden');
        btnReaderNext.classList.add('hidden');
    } else if (currentCapIndex === 0) {
        btnReaderPrev.classList.add('hidden');
        btnReaderNext.classList.remove('hidden');
    } else if (currentCapIndex === totalCaps - 1) {
        btnReaderPrev.classList.remove('hidden');
        btnReaderNext.classList.add('hidden');
    } else {
        btnReaderPrev.classList.remove('hidden');
        btnReaderNext.classList.remove('hidden');
    }
}

function cargarCapitulo(index) {
    const comic = bdComics[currentComicId];
    if (!comic || !comic.links[index]) return;
    currentCapIndex = index;
    pdfViewer.src = comic.links[index];
    actualizarBotonesLector();
}

btnReaderBack.addEventListener('click', () => {
    reader.classList.add('hidden');
    pdfViewer.src = "";
});

btnReaderPrev.addEventListener('click', () => {
    if (currentCapIndex > 0) {
        cargarCapitulo(currentCapIndex - 1);
    }
});

btnReaderNext.addEventListener('click', () => {
    const comic = bdComics[currentComicId];
    if (comic && currentCapIndex < comic.capitulos.length - 1) {
        cargarCapitulo(currentCapIndex + 1);
    }
});

